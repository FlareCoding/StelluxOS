int stlxgfx_draw_char(stlxgfx_surface_t* s, int32_t x, int32_t y,
                      char c, uint32_t fg, uint32_t bg) {
    if (!s || !s->pixels) {
        return -1;
    }

    unsigned char idx = (unsigned char)c;
    if (idx >= 128) {
        idx = '?';
    }

    const unsigned char* glyph = font_8x16[idx];
    uint32_t bytes_pp = s->bpp / 8;
    int has_bg = ((bg >> 24) & 0xFF) != 0;
    uint8_t alpha_byte = 3;
    if (bytes_pp == 4 && s->red_shift != 0 && s->green_shift != 0 && s->blue_shift != 0) {
        alpha_byte = 0;
    }

    for (int row = 0; row < STLXGFX_FONT_HEIGHT; row++) {
        int32_t py = y + row;
        if (py < 0 || py >= (int32_t)s->height) {
            continue;
        }

        unsigned char bits = glyph[row];
        for (int col = 0; col < STLXGFX_FONT_WIDTH; col++) {
            int32_t px = x + col;
            if (px < 0 || px >= (int32_t)s->width) {
                continue;
            }

            int set = (bits >> (7 - col)) & 1;
            if (set) {
                uint8_t* pixel = s->pixels + (uint32_t)py * s->pitch + (uint32_t)px * bytes_pp;
                pixel[s->red_shift   / 8] = (fg >> 16) & 0xFF;
                pixel[s->green_shift / 8] = (fg >>  8) & 0xFF;
                pixel[s->blue_shift  / 8] =  fg        & 0xFF;
                if (bytes_pp == 4) {
                    pixel[alpha_byte] = 0xFF;
                }
            } else if (has_bg) {
                uint8_t* pixel = s->pixels + (uint32_t)py * s->pitch + (uint32_t)px * bytes_pp;
                pixel[s->red_shift   / 8] = (bg >> 16) & 0xFF;
                pixel[s->green_shift / 8] = (bg >>  8) & 0xFF;
                pixel[s->blue_shift  / 8] =  bg        & 0xFF;
                if (bytes_pp == 4) {
                    pixel[alpha_byte] = (bg >> 24) & 0xFF;
                }
            }
        }
    }
    return 0;
}

int stlxgfx_draw_text(stlxgfx_surface_t* s, int32_t x, int32_t y,
                      const char* text, uint32_t fg, uint32_t bg) {
    if (!s || !text) {
        return -1;
    }
    for (int i = 0; text[i]; i++) {
        stlxgfx_draw_char(s, x + i * STLXGFX_FONT_WIDTH, y, text[i], fg, bg);
    }
    return 0;
}

void stlxgfx_text_size(const char* text, uint32_t* out_w, uint32_t* out_h) {
    size_t len = text ? strlen(text) : 0;
    if (out_w) {
        *out_w = (uint32_t)(len * STLXGFX_FONT_WIDTH);
    }
    if (out_h) {
        *out_h = STLXGFX_FONT_HEIGHT;
    }
}
